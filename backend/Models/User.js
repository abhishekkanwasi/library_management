const mongoose = require("mongoose");
const { Schema } = mongoose;
const userSchema = new Schema({
  password: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  role: {
    type: String,
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  }
});
module.exports = mongoose.model("Users", userSchema);
